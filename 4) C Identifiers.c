#include <stdio.h>

int main()
{
	int a=10;
	int A=20;

	printf("Value of a is: %d",a);
	printf("\nValue of A is: %d",A);

	return 0;
}
/*The output shows that the values of both the variables, 
'a' and 'A' are different. Therefore, we conclude that the 
identifiers are case sensitive.*/